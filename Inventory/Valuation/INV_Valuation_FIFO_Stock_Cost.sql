  Используемые таблицы:
  • OITM  - Справочник товаров (себестоимость, метод оценки)
  • OITW  - Остатки по складам (количество, стоимость на складе)
  • ORTT  - Курсы валют (конвертация USD → UZS)

  Связи:
  • OITM.ItemCode = OITW.ItemCode  ← Товар
  
  Логика расчета себестоимости (приоритет):
  1. OITW.StockValue / OnHand - фактическая FIFO стоимость слоев
  2. OITM.AvgPrice           - средняя себестоимость
  3. OITM.LastPurPrc         - последняя цена закупки (fallback)
  
  ПРИМЕЧАНИЕ: Если StockValue = 0, значит FIFO слои не инициализированы
  Для детального отчета по FIFO слоям смотрите manufacturing_cost_detailed_with_layers.sql


