║  INV_Valuation_FIFO_Stock_Cost.sql                                                     ║
╠════════════════════════════════════════════════════════════════════════════════════════╣
║  ОТЧЕТ: СТОИМОСТЬ ТОВАРОВ ПО МЕТОДУ FIFO (USD и UZS)                                  ║
╠════════════════════════════════════════════════════════════════════════════════════════╣
║  ЧТО ПОКАЗЫВАЕТ ЭТОТ ОТЧЕТ:                                                            ║
║  • Текущие остатки товаров по складам                                                  ║
║  • Себестоимость FIFO в USD (системная валюта)                                         ║
║  • Себестоимость FIFO в UZS (по последнему курсу)                                      ║
║                                                                                        ║
║  УСЛОВИЯ:                                                                              ║
║  • Показываются только товары с методом FIFO (EvalSystem = 'F')                        ║
║  • Только складские позиции (InvntItem = 'Y')                                          ║
║  • Только остатки больше 0                                                             ║
║  Tables: OITM (Items), OITW (Item Warehouse), ORTT (Exchange Rates)                    ║
╚════════════════════════════════════════════════════════════════════════════════════════╝
*/

SELECT
    -- ═══════════════════════════════════════════════════════════
    -- БЛОК 1: ИНФОРМАЦИЯ О ТОВАРЕ
    -- ═══════════════════════════════════════════════════════════
    T0."ItemCode"                   AS "Код товара",
    T0."ItemName"                   AS "Название",
    T1."WhsCode"                    AS "Склад",
    T0."InvntryUom"                 AS "Ед.изм",
    
    -- ═══════════════════════════════════════════════════════════
    -- БЛОК 2: ОСТАТКИ И СТОИМОСТЬ (USD)
    -- ═══════════════════════════════════════════════════════════
    T1."OnHand"                     AS "Остаток (кол-во)",
    
    -- Используем приоритет: StockValue > AvgPrice > LastPurPrc
    CASE 
        WHEN T1."StockValue" <> 0 THEN T1."StockValue" / NULLIF(T1."OnHand", 0)
        WHEN T0."AvgPrice" <> 0 THEN T0."AvgPrice"
        ELSE IFNULL(T0."LastPurPrc", 0)
    END                             AS "Себест-ть (USD)",
    
    T1."OnHand" * 
    CASE 
        WHEN T1."StockValue" <> 0 THEN T1."StockValue" / NULLIF(T1."OnHand", 0)
        WHEN T0."AvgPrice" <> 0 THEN T0."AvgPrice"
        ELSE IFNULL(T0."LastPurPrc", 0)
    END                             AS "Остаток (USD)",
    
    -- ═══════════════════════════════════════════════════════════
    -- БЛОК 3: КУРС И СТОИМОСТЬ (UZS)
    -- ═══════════════════════════════════════════════════════════
    R."Rate"                        AS "Курс USD → UZS",
    
    CASE 
        WHEN T1."StockValue" <> 0 THEN (T1."StockValue" / NULLIF(T1."OnHand", 0)) * R."Rate"
        WHEN T0."AvgPrice" <> 0 THEN T0."AvgPrice" * R."Rate"
        ELSE IFNULL(T0."LastPurPrc", 0) * R."Rate"
    END                             AS "Себест-ть (UZS)",
    
    T1."OnHand" * 
    CASE 
        WHEN T1."StockValue" <> 0 THEN (T1."StockValue" / NULLIF(T1."OnHand", 0)) * R."Rate"
        WHEN T0."AvgPrice" <> 0 THEN T0."AvgPrice" * R."Rate"
        ELSE IFNULL(T0."LastPurPrc", 0) * R."Rate"
    END                             AS "Остаток (UZS)"

-- ═══════════════════════════════════════════════════════════════════════════
-- ОТКУДА БЕРЕМ ДАННЫЕ
-- ═══════════════════════════════════════════════════════════════════════════
FROM OITM T0                       -- Справочник товаров

INNER JOIN OITW T1                 -- Остатки по складам
    ON T0."ItemCode" = T1."ItemCode"

-- Последний курс UZS
LEFT JOIN (
    SELECT "Rate" 
    FROM ORTT 
    WHERE "Currency" = 'UZS' 
    ORDER BY "RateDate" DESC 
    LIMIT 1
) R ON 1=1

WHERE 
    T0."InvntItem" = 'Y'           -- Только складские позиции
    AND T0."EvalSystem" = 'F'      -- Только товары с методом FIFO
    AND T1."OnHand" > 0            -- Только положительные остатки

ORDER BY 
    T0."ItemCode", 
    T1."WhsCode";

/*
╔════════════════════════════════════════════════════════════════════════════════════════╗
║                          📖 ИНСТРУКЦИЯ ПО ИСПОЛЬЗОВАНИЮ                                ║
╚════════════════════════════════════════════════════════════════════════════════════════╝

┌────────────────────────────────────────────────────────────────────────────────────────┐
│ ЧТО ПОКАЗЫВАЕТ КАЖДАЯ КОЛОНКА                                                          │
└────────────────────────────────────────────────────────────────────────────────────────┘

  📦 БЛОК 1 - ИНФОРМАЦИЯ О ТОВАРЕ:
    • Код товара        = Код позиции из справочника
    • Название          = Полное название товара
    • Склад             = Код склада
    • Ед.изм            = Единица измерения

  💵 БЛОК 2 - СТОИМОСТЬ В USD (системная валюта):
    • Остаток (кол-во)  = Количество на складе
    • Себест-ть (USD)   = Цена за 1 единицу в долларах
                          (по приоритету: StockValue > AvgPrice > LastPurPrc)
    • Остаток (USD)     = Общая стоимость остатка в долларах

  💰 БЛОК 3 - СТОИМОСТЬ В UZS (узбекских сумах):
    • Курс USD → UZS    = Последний курс доллара к суму
    • Себест-ть (UZS)   = Цена за 1 единицу в сумах
    • Остаток (UZS)     = Общая стоимость остатка в сумах


┌────────────────────────────────────────────────────────────────────────────────────────┐
│ 💡 ВАЖНАЯ ИНФОРМАЦИЯ                                                                   │
└────────────────────────────────────────────────────────────────────────────────────────┘

  ✓ Отчет показывает товары с методом оценки FIFO
  ✓ Себестоимость рассчитывается по приоритету:
    1. OITW.StockValue / OnHand (фактическая складская стоимость FIFO)
    2. OITM.AvgPrice (средняя себестоимость товара)
    3. OITM.LastPurPrc (цена последней закупки - как запасной вариант)
  ✓ Количество берется из OITW."OnHand" (остаток на складе)
  ✓ Стоимость в UZS рассчитывается по последнему курсу из ORTT
  ✓ Показываются только положительные остатки
  
  ⚠️  ВАЖНО: Если StockValue = 0, то система использует LastPurPrc
      Это означает, что FIFO слои не были правильно инициализированы
      Рекомендуется выполнить переоценку запасов в SAP B1


